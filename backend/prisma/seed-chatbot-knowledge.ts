// Format topic content as a Q/R list the LLM can consume directly.
function formatTopicContent(topic: Topic): string {
  return topic.qa
    .filter((p) => p.q.trim())
    .map((p) => (p.a ? `Q: ${p.q}\nR: ${p.a}` : `Q: ${p.q}\nR: (à compléter — répondre selon la connaissance générale)`))
    .join("\n\n");
}

async function main() {
  console.log(`Seeding ${TOPICS.length} chatbot knowledge topics…`);

  for (const topic of TOPICS) {
    const content = formatTopicContent(topic);
    // Upsert by title (treat title as natural key for idempotency).
    const existing = await prisma.chatbotTrainingDocument.findFirst({
      where: { title: topic.title },
    });
    if (existing) {
      await prisma.chatbotTrainingDocument.update({
        where: { id: existing.id },
        data: {
          titleAr: topic.titleAr,
          content,
          category: topic.category,
          isActive: true,
        },
      });
      console.log(`  ↻ updated  "${topic.title}" (${topic.qa.length} Q/R)`);
    } else {
      await prisma.chatbotTrainingDocument.create({
        data: {
          title: topic.title,
          titleAr: topic.titleAr,
          content,
          category: topic.category,
          isActive: true,
        },
      });
      console.log(`  + inserted "${topic.title}" (${topic.qa.length} Q/R)`);
    }
  }

  const total = TOPICS.reduce((acc, t) => acc + t.qa.length, 0);
  console.log(`\nDone. ${TOPICS.length} topics, ${total} Q/R pairs total.`);
  await prisma.$disconnect();
}

main().catch(async (err) => {
  console.error(err);
  await prisma.$disconnect();
  process.exit(1);
});
